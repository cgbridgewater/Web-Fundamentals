// examples of data types and how to get them

var caden = {
    name: "Caden",
    age: 24,
    email: "cwilcox@codingdojo.com",
    isHuman: false,
    favHobbies: ["Gaming","Playing music","Coding"],
}
// .name pulls just the name data
console.log(caden.name);


// how to pull multiple lines of data 
console.log(`${caden.name} is ${caden.age}`);

// how to pull the first item in an array from a line
console.log(caden.favHobbies[1]);

